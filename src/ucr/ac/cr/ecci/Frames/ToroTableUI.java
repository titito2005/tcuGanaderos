    public void redrawTorosTable(String query) {
        bovinoQuery = TCUGanaderosPUEntityManager.createQuery(query);
        bovinoList = bovinoQuery.getResultList();
        torosTable.setColumnSelectionAllowed(false);
        bindingGroup = new org.jdesktop.beansbinding.BindingGroup();
        org.jdesktop.swingbinding.JTableBinding jTableBinding = org.jdesktop.swingbinding.SwingBindings.createJTableBinding(org.jdesktop.beansbinding.AutoBinding.UpdateStrategy.READ_WRITE, bovinoList, torosTable);
        org.jdesktop.swingbinding.JTableBinding.ColumnBinding columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${id}"));
        columnBinding.setColumnName("Id");
        columnBinding.setColumnClass(Integer.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${nombre}"));
        columnBinding.setColumnName("Nombre");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${fechaNacimiento}"));
        columnBinding.setColumnName("Fecha Nacimiento");
        columnBinding.setColumnClass(java.util.Date.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${peso}"));
        columnBinding.setColumnName("Peso");
        columnBinding.setColumnClass(Double.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${caracteristicas}"));
        columnBinding.setColumnName("Caracteristicas");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${esBaja}"));
        columnBinding.setColumnName("Es Baja");
        columnBinding.setColumnClass(Boolean.class);
        bindingGroup.addBinding(jTableBinding);
        jTableBinding.bind();
        jScrollPane1.setViewportView(torosTable);
        if (torosTable.getColumnModel().getColumnCount() > 0) {
            torosTable.getColumnModel().getColumn(0).setPreferredWidth(6);
            torosTable.getColumnModel().getColumn(1).setPreferredWidth(50);
            torosTable.getColumnModel().getColumn(2).setPreferredWidth(50);
            torosTable.getColumnModel().getColumn(3).setPreferredWidth(50);
            torosTable.getColumnModel().getColumn(4).setPreferredWidth(6);
            torosTable.getColumnModel().getColumn(5).setPreferredWidth(10);
        }
        showingBajas = false;
        bajarButton.setVisible(true);
        rowSorter = new TableRowSorter<>(torosTable.getModel());
        torosTable.setRowSorter(rowSorter);
        jScrollPane1.repaint();
    }

