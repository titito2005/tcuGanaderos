    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (serviciosTable.getSelectedRow() != -1) {
            int des = JOptionPane.showConfirmDialog(null, "¿Está seguro que quiere eliminar este servicio? La acción no es reversible.","Eliminar Servicio", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(des==0){
                DBManager dbm = new DBManager();
                Servicio servicioLocal = servicioList.get(serviciosTable.getSelectedRow());
                Servicio servicioDB = dbm.findServicioById(servicioLocal.getIdServicio());
                dbm.delete(servicioDB);
                dbm.close();
                redrawServiciosTable();
            }
        }else{
            JOptionPane.showMessageDialog(new JFrame(),"Debe seleccionar un servicio para eliminar.","Error de operación",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    
     private void redrawServiciosTable() {
        servicioQuery = TCUGanaderosPUEntityManager.createQuery("SELECT s FROM Servicio s WHERE s.idVaca.idBovino = " + bovino_id + " ORDER BY s.fechaServicio DESC");
        servicioList = servicioQuery.getResultList();
        serviciosTable.setColumnSelectionAllowed(false);
        bindingGroup = new org.jdesktop.beansbinding.BindingGroup();
        org.jdesktop.swingbinding.JTableBinding jTableBinding = org.jdesktop.swingbinding.SwingBindings.createJTableBinding(org.jdesktop.beansbinding.AutoBinding.UpdateStrategy.READ_WRITE, servicioList, serviciosTable);
        org.jdesktop.swingbinding.JTableBinding.ColumnBinding columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${fechaServicio}"));
        columnBinding.setColumnName("Fecha");
        columnBinding.setColumnClass(java.util.Date.class);
        columnBinding.setEditable(false);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${idServicio}"));
        columnBinding.setColumnName("ID Servicio");
        columnBinding.setColumnClass(Integer.class);
        columnBinding.setEditable(false);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${idToro}"));
        columnBinding.setColumnName("Padre");
        columnBinding.setColumnClass(Integer.class);
        columnBinding.setEditable(false);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${estaPreñada}"));
        columnBinding.setColumnName("Está preñada");
        columnBinding.setColumnClass(Boolean.class);
        columnBinding.setEditable(false);
        bindingGroup.addBinding(jTableBinding);
        jTableBinding.bind();
        jScrollPane1.setViewportView(serviciosTable);
        if (serviciosTable.getColumnModel().getColumnCount() > 0) {
            serviciosTable.getColumnModel().getColumn(0).setResizable(false);
            serviciosTable.getColumnModel().getColumn(0).setPreferredWidth(45);
            serviciosTable.getColumnModel().getColumn(1).setResizable(false);
            serviciosTable.getColumnModel().getColumn(2).setResizable(false);
            serviciosTable.getColumnModel().getColumn(3).setResizable(false);
        }
    
    }
