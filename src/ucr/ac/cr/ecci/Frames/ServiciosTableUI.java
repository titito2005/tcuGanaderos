    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (serviciosTable.getSelectedRow() != -1) {
            new ServicioUI((Servicio) servicioList.get(serviciosTable.getSelectedRow()), this.bovino).setVisible(true);
            setVisible(false);
            dispose();
        }else{
            JOptionPane.showMessageDialog(new JFrame(),"Debe seleccionar un servicio a editar.","Error de operación",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

