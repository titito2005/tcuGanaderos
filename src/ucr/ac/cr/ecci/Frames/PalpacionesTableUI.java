    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        if (palpacionesTable.getSelectedRow() != -1) {
            DBManager dbm = new DBManager();
            Palpacion palpacionLocal = palpacionList.get(palpacionesTable.getSelectedRow());
            Palpacion palpacionDB = dbm.findPalpacionById(palpacionLocal.getId());
            dbm.delete(palpacionDB);
            dbm.close();
            redrawPalpacionesTable();
        }
    }//GEN-LAST:event_eliminarButtonActionPerformed

