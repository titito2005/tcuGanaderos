    private void redrawPalpacionesTable() {
        palpacionQuery = TCUGanaderosPUEntityManager.createQuery("SELECT p FROM Palpacion p WHERE p.idBovino.id = " + bovino_id);
        palpacionList = palpacionQuery.getResultList();
        palpacionesTable.setColumnSelectionAllowed(false);
        bindingGroup = new org.jdesktop.beansbinding.BindingGroup();
        org.jdesktop.swingbinding.JTableBinding jTableBinding = org.jdesktop.swingbinding.SwingBindings.createJTableBinding(org.jdesktop.beansbinding.AutoBinding.UpdateStrategy.READ_WRITE, palpacionList, palpacionesTable);
        org.jdesktop.swingbinding.JTableBinding.ColumnBinding columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${fecha}"));
        columnBinding.setColumnName("Fecha");
        columnBinding.setColumnClass(java.util.Date.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${condicionCorporal}"));
        columnBinding.setColumnName("Condicion Corporal");
        columnBinding.setColumnClass(Double.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${confirmacion}"));
        columnBinding.setColumnName("Confirmacion");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${resultado}"));
        columnBinding.setColumnName("Resultado");
        columnBinding.setColumnClass(String.class);
        bindingGroup.addBinding(jTableBinding);
        jTableBinding.bind();
        jScrollPane1.setViewportView(palpacionesTable);
        if (palpacionesTable.getColumnModel().getColumnCount() > 0) {
            palpacionesTable.getColumnModel().getColumn(0).setResizable(false);
            palpacionesTable.getColumnModel().getColumn(1).setResizable(false);
            palpacionesTable.getColumnModel().getColumn(1).setPreferredWidth(10);
            palpacionesTable.getColumnModel().getColumn(2).setResizable(false);
            palpacionesTable.getColumnModel().getColumn(3).setResizable(false);
            palpacionesTable.getColumnModel().getColumn(3).setPreferredWidth(200);
        }

    }

