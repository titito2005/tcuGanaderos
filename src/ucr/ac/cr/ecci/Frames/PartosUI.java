    private void guardarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarButtonActionPerformed
        DBManager dbm = new DBManager();
        boolean guardar=true;
        parto = new Parto();
        parto.setIdMadre(bovino);
        
        if( padreComboBox.getSelectedItem()!=null){
            Bovino bovinoPadreLocal = (Bovino) padreComboBox.getSelectedItem();
            Bovino bovinoPadreDB = dbm.findBovinoById(bovinoPadreLocal.getId());
            parto.setIdPadre(bovinoPadreDB);
            
            if (mRadioButton.isSelected()){
                parto.setSexo("Masculino");
            }
            if (fRadioButton.isSelected()){
                parto.setSexo("Femenino");
            }
            
            if (fechaDatePicker.getDate()!=null){//FECHA
                Calendar cal = Calendar.getInstance();
                Date date= fechaDatePicker.getDate();
                Date date2= cal.getTime();
                if(date.before(date2)){
                    parto.setFecha(fechaDatePicker.getDate());
                }else{
                    JOptionPane.showMessageDialog(new JFrame(),"La fecha del parto no es v치lida, debe agregar una fecha anterior o igual a la actual.","Error de operaci칩n",JOptionPane.ERROR_MESSAGE);
                    guardar=false;
                }
            }else{
                JOptionPane.showMessageDialog(new JFrame(),"Debe agregar una fecha de parto.","Error de operaci칩n",JOptionPane.ERROR_MESSAGE);
                guardar=false;
            }
            
            if (muerteCheckBox.isSelected()){
                parto.setMuerteprematura(true);
            }else{
                parto.setMuerteprematura(false);
            }
            
            if(guardar){
                dbm.insert(parto);
                dbm.close();  
                setVisible(false);
                dispose();            
            }
        }else{
            JOptionPane.showMessageDialog(new JFrame(),"Debe agregar un toro a la base de datos para poder asignarlo como padre.","Error de operaci칩n",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_guardarButtonActionPerformed

